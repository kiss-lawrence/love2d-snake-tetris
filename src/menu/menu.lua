function Menu:new()
	local inst = {}
	setmetatable(inst, self)
	inst.screen = Screen:new()
	inst.leftArrow = Object:new()
	inst.rightArrow = Object:new()
	inst.game = 0
	inst.pressing = ""
	return inst
end

function Menu:setArrow()
	self.leftArrow:copy(Menu.picture.leftArrow)
	self.leftArrow:moveTo(Menu.picture.leftArrowX, Menu.picture.leftArrowY)
	self.rightArrow:copy(Menu.picture.rightArrow)
	self.rightArrow:moveTo(Menu.picture.rightArrowX, Menu.picture.rightArrowY)
end

function Menu:setGame(game)
	self.game = game
	self.screen:copy(Menu.picture[self.game])
end

function Menu:draw()
	self.leftArrow:draw()
	self.rightArrow:draw()
	self.screen:draw()
end

function Menu:keypressed(key)
	if self.pressing ~= "" then
		return
	end

	if key == "left" then
		self.leftArrow:move(-1, 0)
		self.pressing = key
	elseif key == "right" then
		self.rightArrow:move(1, 0)
		self.pressing = key
	
	elseif key == "return" or key == "kpenter" or key == " " then
		self.pressing = key
	end
end

function Menu:keyreleased(key)
	if self.pressing ~= key then
		return
	end

	if key == "left" then
		self.leftArrow:move(1, 0)
		local newGame = self.game
		if newGame - 1 == 0 then
			newGame = #Menu.index
		else
			newGame = newGame - 1
		end
		self:setGame(newGame)
		self.pressing = ""
	elseif key == "right" then
		self.rightArrow:move(-1, 0)
		local newGame = self.game
		if newGame + 1 > #Menu.index then
			newGame = 1
		else
			newGame = newGame + 1
		end
		self:setGame(newGame)
		self.pressing = ""
	
	elseif key == "return" or key == "kpenter" or key == " " then
		self.pressing = ""
		return self.game
	end
end




























